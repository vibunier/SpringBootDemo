/**
 * Definition for singly-linked list.
 */
class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }

    public String toString() {
        ListNode temp = this;
        StringBuilder sb = new StringBuilder();
        sb.append(temp.val);
        while (temp.next != null) {
            sb.append("->");
            temp = temp.next;
            sb.append(temp.val);
        }
        return sb.toString();
    }
}

